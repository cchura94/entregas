    protected function create_usuario(Request $request)
    {
        
        $request->validate([
            'name' => ['required', 'string', 'max:255'],
            'email' => ['required', 'string', 'email', 'max:255', 'unique:users'],
            'password' => ['required', 'string', 'min:8', 'confirmed'],
        ]);

        $u = User::create([
            'name' => $request->name,
            'email' => $request->email,
            'password' => Hash::make($request->password),
        ]);


$rol_ejecucion = Role::where('nombre', 'ejecucion')->first();
$u->roles()->attach($rol_ejecucion);
        return redirect('/admin/repartidor');
    }

